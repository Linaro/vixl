void IOReporter::Visit(Metadata* metadata, const Instruction* instr) {
  VIXL_ASSERT(metadata->count("form") > 0);

  const std::string& form = (*metadata)["form"];
  uint32_t form_hash = Hash(form.c_str());
  const FormToRegisterUseMap* fr = IOReporter::GetFormToRegisterUseMap();
  FormToRegisterUseMap::const_iterator it = fr->find(form_hash);
  if (it == fr->end()) {
    // TODO: how should a missing entry be reported? Assertion failure?
    // An error polled from the IOReporter object?
  } else {
    AddRegistersFromTokens(instr, it->second);
  }
}

void IOReporter::AddRegistersFromTokens(const Instruction* instr,
                                        const std::string& s) {
  // All token strings contain a separator ',' with destination register tokens
  // to the left and sources to the right. Either set may be empty.
  VIXL_ASSERT(s.find(',') != std::string::npos);

  // Match tokens in the string. See the header file for a description of the
  // tokens.
  std::regex re("'([RVPI][^',]+)");
  std::string haystack = s;
  std::smatch m;
  bool is_dst = true;

  // Disabling linter on the following line due to possible clang-tidy bug.
  // TODO: remove this when switching baseline linter version.
  while (std::regex_search(haystack, m, re)) {  // NOLINT
    // The first token match found at the second character in a string indicates
    // that a comma must be the first character. At that point, we switch to
    // finding source tokens.
    if (m.position() != 0) {
      VIXL_ASSERT(haystack[0] == ',');
      is_dst = false;
    }

    CPURegister r = GetRegisterForToken(instr, m.str(1));
    if (r.IsValid()) {
      if (is_dst) {
        destination_.insert(r);
      } else {
        source_.insert(r);
      }
    }

    haystack = m.suffix().str();
  }
}

CPURegister IOReporter::GetRegisterForToken(const Instruction* instr,
                                            const std::string& t) {
  VIXL_ASSERT((t[0] == 'R') || (t[0] == 'V') || (t[0] == 'P') || (t[0] == 'I'));

  // The token's register "name" indicates where to look in the instruction for
  // the register used. There are basically four positions, but these may be
  // modified (eg. by addition) or restricted (eg. being encoded in four
  // rather than five bits) by later token characters. There is also the ability
  // to express an explicit range of bits to look at for the register, if
  // needed.
  int code = -1;
  switch (t[1]) {
    case 'd':
      code = instr->GetRd();
      break;
    case 'n':
      code = instr->GetRn();
      break;
    case 'm':
      code = instr->GetRm();
      break;
    case 'a':
      code = instr->GetRa();
      break;
    case 'g':
      VIXL_ASSERT(t[0] == 'P');
      code = instr->GetPgLow8();
      break;
    default:
      if (t.length() == 5) {  // Explicit bit range, eg. V1816.
        // Extract bit positions and get code from the instruction.
        uint32_t msb = GetFiveBitNumberFromString(t.substr(1));
        uint32_t lsb = GetFiveBitNumberFromString(t.substr(3));
        code = instr->ExtractBits(msb, lsb);
      } else {  // Register implied by the instruction, eg. I30.
        VIXL_ASSERT(t[0] == 'I');
        code = GetFiveBitNumberFromString(t.substr(1));
      }
  }
  VIXL_ASSERT(code != -1);

  // Registers that depend on a bit being set in the instruction to indicate
  // whether it should be returned.
  if (t[2] == '?') {  // Pd?04
    int bit = GetFiveBitNumberFromString(t.substr(3));
    if (instr->ExtractBit(bit) == 0) {
      // The bit was clear, so report nothing for this token.
      return NoCPUReg;
    }
  }

  if (t[0] == 'R') {    // Integer bank.
    if (t[2] == '+') {  // Rm+1, Rm+2 ... Rm+7.
      VIXL_ASSERT((t[3] >= '1') && (t[3] <= '7'));
      code = (code + (t[3] - '0')) % 32;
    } else if (t[2] == 's') {  // Code 31 is stack pointer.
      code = (code == 31) ? kSPRegInternalCode : code;
    }

    if (code == 31) {
      return NoCPUReg;  // Discard zero register.
    }
    return XRegister(code);
  } else if (t[0] == 'V') {  // Vector bank.
    if (t[2] == 'i') {       // Vmi - indexed vector register.
      int size = instr->GetNEONSize();
      // The element size field may be 1 or 2, otherwise the instruction is
      // unallocated.
      if ((size != 1) && (size != 2)) {
        return NoCPUReg;
      }
      if (size == 1) {
        code %= 16;  // Only 16 registers may be indexed for this size.
      }
    } else if (t[2] == '+') {  // Vn+1, Vn+2 or Vn+3.
      VIXL_ASSERT((t[3] >= '1') && (t[3] <= '3'));
      code = (code + (t[3] - '0')) % 32;
    }
    return VRegister(code);
  } else if (t[0] == 'P') {  // Predicate bank.
    code %= 16;              // There are 16 predicate registers.
    return PRegister(code);
  } else if (t[0] == 'I') {  // Implicit register, assumes integer bank.
    return XRegister((code == 31) ? kSPRegInternalCode : code);
  }

  // No token should reach here.
  VIXL_UNREACHABLE();
  return NoCPUReg;
}

uint32_t IOReporter::GetFiveBitNumberFromString(const std::string& s) {
  VIXL_ASSERT(s.length() >= 2);
  VIXL_ASSERT((s[0] >= '0') && (s[0] <= '3'));
  VIXL_ASSERT((s[1] >= '0') && (s[1] <= '9'));
  uint32_t code = ((s[0] - '0') * 10) + (s[1] - '0');
  VIXL_ASSERT(code <= 31);
  return code;
}

